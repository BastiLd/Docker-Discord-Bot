def normalize_locale(locale: str | None) -> str:
    if locale in SUPPORTED_LOCALES:
        return locale
    return DEFAULT_LOCALE


def translations_for(locale: str | None) -> dict[str, str]:
    normalized = normalize_locale(locale)
    base = TRANSLATIONS[DEFAULT_LOCALE].copy()
    if normalized != DEFAULT_LOCALE:
        base.update(TRANSLATIONS[normalized])
    return base


def translate(locale: str | None, key: str, **kwargs: Any) -> str:
    text = translations_for(locale).get(key, key)
    return text.format(**kwargs) if kwargs else text


def translator(locale: str | None):
    return partial(translate, normalize_locale(locale))


def locale_cookie_value(raw_value: str | None) -> str:
    return normalize_locale(raw_value)


